public class SystemConfig {

	private String companyFolder;
	private String companyDefault;
	private String taskDefault;
	private String templateDefault;
	private String attachmentDefault;

	public String getCompanyFolder() {
		return companyFolder;
	}

	public void setCompanyFolder(String companyFolder) {
		this.companyFolder = companyFolder;
	}

	public String getCompanyDefault() {
		return companyDefault;
	}

	public void setCompanyDefault(String companyDefault) {
		this.companyDefault = companyDefault;
	}

	public String getTaskDefault() {
		return taskDefault;
	}

	public void setTaskDefault(String taskDefault) {
		this.taskDefault = taskDefault;
	}

	public String getTemplateDefault() {
		return templateDefault;
	}

	public void setTemplateDefault(String templateDefault) {
		this.templateDefault = templateDefault;
	}

	public String getAttachmentDefault() {
		return attachmentDefault;
	}

	public void setAttachmentDefault(String attachmentDefault) {
		this.attachmentDefault = attachmentDefault;
	}

}
